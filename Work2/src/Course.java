
public class Course {
	
	int id;
	String name;
	String percentageOfCompletion;
	String Instructor;
	
	public Course() {
		
	}
	
	public Course(int id, String name, String percentageOfCompletion, String Instructor ) {
		this.id = id;
		this.Instructor = Instructor;
		this.name = name;
		this.percentageOfCompletion = percentageOfCompletion;
				
	}
	

}
